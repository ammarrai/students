package core.array;

public class MultiDimensionalArray {

	public static void main(String[] args) {

		// declare array
		String[][][] multiDimen;

		// instantiate array
		multiDimen = new String[2][2][2];

		// initialize array
		multiDimen[0][0][0] = "bob";
		multiDimen[0][0][1] = "jim";
		multiDimen[0][0][2] = "tim";
		
	}
}
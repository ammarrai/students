package core.constructorsandinitializers.parameters;

public class Flight {

	// instance variables
	int flightNumber;

	// constructors
	public Flight(int flightNumber) {
		this.flightNumber = flightNumber;
	}

	// g&s
	public int getFlightNumber() {
		return flightNumber;
	}

	public void setFlightNumber(int flightNumber) {
		this.flightNumber = flightNumber;
	}
}
